def ordinal_suffix(n):
    str_num = str(n)
    last_str_num = str_num[-1]
    if (last_str_num == '1' and n != 11):
        return str_num + 'st'
    if (last_str_num == '2' and n != 12):
        return str_num + 'nd'
    if (last_str_num == '3' and n != 13):
        return str_num + 'rd'

    return str_num + 'th'


